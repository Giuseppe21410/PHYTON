## Tipos de errores ## 
# 1. Errores de sintaxis: Son errores que ocurren cuando el código no sigue las reglas del lenguaje de programación.
#    Ejemplo:

#print("Hola, mundo!"  # Falta un paréntesis de cierre.
    
# 2. Errores de tiempo de ejecución: Son errores que ocurren durante la ejecución del programa, a menudo debido a condiciones inesperadas.
#    Ejemplo:

x = 10
y = 0
#z = x / y  # División por cero, lo que genera un error de tiempo de ejecución.

# 3. NameError: Ocurren cuando se intenta usar una variable que no ha sido definida o inicializada.
#    Ejemplo:

# print(variable_no_definida)  # Intento de imprimir una variable que no ha sido definida.

# 4. TypeError: Ocurren cuando se intenta realizar una operación en un tipo de dato no compatible.

#    Ejemplo:

#print("Hola" + 5)  # Intento de concatenar una cadena con un número entero.


# 5. IndexError: Ocurren cuando se intenta acceder a un índice que está fuera del rango de una lista o una cadena.

#    Ejemplo:

lista = [1, 2, 3]

# print(lista[5])  # Intento de acceder a un índice que no existe en la lista.

#6. Errores de lógica: Son errores que no generan mensajes de error, pero hacen que el programa no funcione como se esperaba.

# 7. ModuleNotFoundError: Ocurren cuando se intenta importar un módulo que no está instalado o no se encuentra en el entorno de Python.
#    Ejemplo:

#import numpy  # Intento de importar un módulo que no está instalado.

# 8. AttributeError: Ocurren cuando se intenta acceder a un atributo o método que no existe en un objeto.

#    Ejemplo:
import math

# print(math.non_existent_method())  # Intento de llamar a un método que no existe en el módulo math.

# 9. KeyError: Ocurren cuando se intenta acceder a una clave que no existe en un diccionario.
#    Ejemplo:

diccionario = {"a": 1, "b": 2}
# print(diccionario["c"])  # Intento de acceder a una clave que no existe en el diccionario.


# 10. ValueError: Ocurren cuando se intenta convertir un valor a un tipo de dato que no es compatible.
#print(list("a"))  # Intento de convertir una clave a una lista, lo que genera un ValueError.

# 11.ImportError: Ocurren cuando se intenta importar un módulo que no existe o no se encuentra en el entorno de Python.

# from math import non_existent_function  # Intento de importar una función que no existe en el módulo math.








